        @Nullable
        public static JLabel getLabelWithFontAwesomeIcon(String symbol) {
            try {
                InputStream is = FontAwesome.class.getResourceAsStream("/fonts/fontawesome.ttf");
                Font font = Font.createFont(Font.TRUETYPE_FONT, is);
                font = font.deriveFont(Font.PLAIN, 24f);
                JLabel label = new JLabel(symbol);
                label.setFont(font);
                return label;
            } catch (FontFormatException | IOException e) {
                e.printStackTrace();
            }
            return null;
        }

